# Faça um programa que leia um número inteiro qualquer e mostre na tela a sua tabuada.

print('Programa que calcula a tabuada de um valor inteiro')
n = int(input('Olá, qual o número que você quer saber a tabuada?'))
print('Conheça a tabuada do {}:' .format(n))
print('{}X1={} \n{}X2={} \n{}X3={} \n{}X4={} \n{}X5={} \n{}X6={} \n{}X7={} \n{}X8={} \n{}X9={} \n{}X10={}'.format(n, n*1, n, n*2, n, n*3, n, n*4, n, n*5, n, n*6, n, n*7, n, n*8, n, n*9, n, n*10))
print('OBRIGADO!')
